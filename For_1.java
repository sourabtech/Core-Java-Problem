package Chapter_8_Conditional_Statement;

public class For_1 {

	public static void main(String[] args) {
	
		//print 100 numbers
		
		for(int i=1; i<=100;i++) // (initialize, condition, increment/decrement)
			System.out.println(i);
		
		for(int j=100; j>=1; j--)
			System.out.println(j);
	}

}
